end

function c9009.sinsumfilter(c)
	return c:IsCode(35952884) and c:IsAbleToGraveAsCost()
end
function c9009.sinsumcon(e,c)
	if c==nil then return true end
	return Duel.GetLocationCount(c:GetControler(),LOCATION_MZONE)>0
		and Duel.IsExistingMatchingCard(c9009.sinsumfilter,c:GetControler(),LOCATION_EXTRA,0,1,nil)
end
function c9009.sinsumop(e,tp,eg,ep,ev,re,r,rp,c)
	local tc=Duel.GetFirstMatchingCard(c9009.sinsumfilter,tp,LOCATION_EXTRA,0,nil)
	Duel.SendtoGrave(tc,REASON_COST)
end
function c9009.negatecon(e,tp,eg,ep,ev,re,r,rp)
	return not e:GetHandler():IsStatus(STATUS_BATTLE_DESTROYED) and Duel.IsChainNegatable(ev)
end
function c9009.negatetg(e,tp,eg,ep,ev,re,r,rp,chk)
	if chk==0 then return true end
	Duel.SetOperationInfo(0,CATEGORY_NEGATE,eg,1,0,0)
	if re:GetHandler():IsDestructable() and re:GetHandler():IsRelateToEffect(re) then
		Duel.SetOperationInfo(0,CATEGORY_DESTROY,eg,1,0,0)
	end
end
function c9009.negateop(e,tp,eg,ep,ev,re,r,rp)
	Duel.NegateActivation(ev)
	if re:GetHandler():IsRelateToEffect(re) then
		Duel.Destroy(eg,REASON_EFFECT)
	end
end
function c9009.selfdestroycon(e)
	return not Duel.IsEnvironment(27564031)
end
