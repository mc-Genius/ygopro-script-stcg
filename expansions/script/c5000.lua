function c5000.initial_effect(c)
	if not c5000.global_check then
		c5000.global_check=true
		local ge1=Effect.GlobalEffect()
		ge1:SetType(EFFECT_TYPE_FIELD+EFFECT_TYPE_CONTINUOUS)
		ge1:SetProperty(EFFECT_FLAG_CANNOT_DISABLE+EFFECT_FLAG_UNCOPYABLE)
		ge1:SetCode(EVENT_PREDRAW)
		ge1:SetOperation(c5000.op)
		Duel.RegisterEffect(ge1,0)
	end
end
function c5000.op(e,tp,eg,ep,ev,re,r,rp)
	local g=Duel.GetFieldGroup(0,0xdf,0xdf)
	Duel.SendtoDeck(g,nil,-2,REASON_RULE)
	local d1,d2,d3,d4,d5=Duel.TossDice(0,5)
	local d6,d7,d8,d9,d10=Duel.TossDice(1,5)
	math.randomseed((d1+d2*10+d3*100+d4*1000+d5*10000+d6*100000+d7*1000000+d8*10000000+d9*100000000+d10*100000000)*g:GetCount())
	local deck=Group.CreateGroup()
	for p=0,1 do
		local max=#c5000.card
		for i=1,40 do
			local sc=Duel.CreateToken(p,c5000.card[math.random(1,max)])
			deck:AddCard(sc)
		end
		max=#c5000.card_ex
		for i=1,15 do
			local sc=Duel.CreateToken(p,c5000.card_ex[math.random(1,max)])
			deck:AddCard(sc)
		end
	end
	Duel.SendtoDeck(deck,nil,1,REASON_RULE)
	for p=0,1 do
		if Duel.SelectYesNo(p,aux.Stringid(5000,0)) then
			local cg=Duel.GetFieldGroup(p,LOCATION_DECK,0)
			Duel.ConfirmCards(p,cg)
		end
		Duel.ShuffleDeck(p)
		Duel.Draw(p,5,REASON_RULE)
	end
	e:Reset()
end
